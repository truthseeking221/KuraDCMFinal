function rowKey(row: DeltaLabRow): string {
  return `${row.section}||${row.test}`;
}

const deltaRowsByKey = new Map(deltaLabRows.map((row) => [rowKey(row), row]));

export function getDeltaLabRow(key: string): DeltaLabRow | undefined {
  return deltaRowsByKey.get(key);
}

export type DeltaLabResult = { date: string; value: string; unit: string; reference: string };

export type DeltaLabFact = {
  key: string;
  label: string;
  value: string;
  date: string;
  shortDate: string;
  reference: string;
  status: string;
  repeatStatus: string;
  summary: string;
};

export function getDeltaLatestResult(key: string): DeltaLabResult | null {
  const row = getDeltaLabRow(key);
  if (!row) return null;

  const date = deltaLabDates.find((candidate) => row.values[candidate]);
  if (!date) return null;

  return {
    date,
    value: row.values[date],
    unit: row.unit,
    reference: row.reference,
  };
}

export function formatDeltaLabDate(date: string, style: "long" | "short" = "long"): string {
  const [year, month, day] = date.split("-").map(Number);
  const parsed = new Date(Date.UTC(year, month - 1, day));
  return new Intl.DateTimeFormat("en-US", {
    month: "short",
    day: "numeric",
    ...(style === "long" ? { year: "numeric" } : {}),
    timeZone: "UTC",
  }).format(parsed);
}

export function formatDeltaLabValue(value: string, unit: string): string {
  if (!unit) return value;
  if (unit === "%") return `${value}%`;
  return `${value} ${unit}`;
}

function requireDeltaLabResult(key: string, label: string): DeltaLabResult {
  const result = getDeltaLatestResult(key);
  if (!result) throw new Error(`Missing delta lab fixture result for ${label}`);
  return result;
}

function createDeltaLabFact({
  key,
  label,
  status,
  summaryStatus,
}: {
  key: string;
  label: string;
  status: string;
  summaryStatus?: string;
}): DeltaLabFact {
  const result = requireDeltaLabResult(key, label);
  const date = formatDeltaLabDate(result.date);
  const value = formatDeltaLabValue(result.value, result.unit);
  const isLatestDraw = result.date === deltaLabDates[0];
  const repeatStatus = isLatestDraw ? status : `not repeated since ${date}`;
  const finalSummaryStatus = summaryStatus ?? (isLatestDraw ? status : "not repeated");

  return {
    key,
    label,
    value,
    date,
    shortDate: formatDeltaLabDate(result.date, "short"),
    reference: result.reference,
    status,
    repeatStatus,
    summary: `${label} ${value} · ${date} · ${finalSummaryStatus}`,
  };
}

