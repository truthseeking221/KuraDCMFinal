type DeltaLabRow = {
  section: string;
  test: string;
  unit: string;
  reference: string;
  values: Record<string, string>;
};

function parseDeltaLabCsv(raw: string): DeltaLabRow[] {
  const [headerLine, ...lines] = raw.trim().split(/\r?\n/);
  const dateColumns = headerLine.split(",").slice(4);

  return lines.map((line) => {
    const cells = line.split(",");
    const values: Record<string, string> = {};
    dateColumns.forEach((date, index) => {
      values[date] = (cells[4 + index] ?? "").trim();
    });

    return {
      section: (cells[0] ?? "").trim(),
      test: (cells[1] ?? "").trim(),
      unit: (cells[2] ?? "").trim(),
      reference: (cells[3] ?? "").trim(),
      values,
    };
  });
}

